package com.design.pattern.model;

public class Television extends Product {
	private String televisionType;
	private float televisionCost;

	public String getTelevisionType() {
		return televisionType;
	}

	public void setTelevisionType(String televisionType) {
		this.televisionType = televisionType;
	}

	public float getTelevisionCost() {
		return televisionCost;
	}

	public void setTelevisionCost(float televisionCost) {
		this.televisionCost = televisionCost;
	}

}
